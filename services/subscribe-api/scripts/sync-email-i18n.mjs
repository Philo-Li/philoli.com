#!/usr/bin/env node
// Extracts the `subscribeEmail` block from src/i18n/*.json and writes
// services/subscribe-api/src/i18n.ts so the worker can render emails
// in the subscriber's chosen language.
import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, '..', '..', '..');
const i18nDir = join(repoRoot, 'src', 'i18n');
const outFile = join(repoRoot, 'services', 'subscribe-api', 'src', 'i18n.ts');

const REQUIRED_KEYS = ['confirmSubject', 'confirmHeading', 'confirmBody', 'confirmCta', 'confirmFallback', 'footerUnsubscribe'];

const locales = readdirSync(i18nDir)
  .filter(f => f.endsWith('.json'))
  .map(f => f.replace(/\.json$/, ''))
  .sort();

const out = {};
const missing = [];

for (const lang of locales) {
  const data = JSON.parse(readFileSync(join(i18nDir, `${lang}.json`), 'utf8'));
  const block = data.subscribeEmail;
  if (!block) {
    missing.push(lang);
    continue;
  }
  const trimmed = {};
  for (const k of REQUIRED_KEYS) {
    if (typeof block[k] !== 'string') {
      throw new Error(`${lang}.json: subscribeEmail.${k} missing or non-string`);
    }
    trimmed[k] = block[k];
  }
  out[lang] = trimmed;
}

if (missing.length) {
  console.warn(`No subscribeEmail block in: ${missing.join(', ')} (will fall back to en)`);
}

const banner = `// Auto-generated by services/subscribe-api/scripts/sync-email-i18n.mjs
// Do not edit by hand. Run \`npm run subscribe:sync-i18n\` from the repo root.
`;

const interfaceFields = REQUIRED_KEYS.map(k => `  ${k}: string;`).join('\n');
const body = `export interface SubscribeEmailStrings {
${interfaceFields}
}

export const EMAIL_I18N: Record<string, SubscribeEmailStrings> = ${JSON.stringify(out, null, 2)};
`;

writeFileSync(outFile, `${banner}\n${body}`, 'utf8');
console.log(`Wrote ${Object.keys(out).length} locales to ${outFile}`);
